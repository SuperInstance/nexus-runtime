def regenerate_golden_references() -> dict[str, tuple[float, int]]:
    """Regenerate all golden references by running vectors through the engine.

    Use this when the engine algorithm changes to update the golden references.
    Returns a dict mapping vector name to (final_score, final_level).
    """
    results: dict[str, tuple[float, int]] = {}
    for vector in TRUST_TEST_VECTORS:
        score, level = run_vector(vector.events, vector.subsystem)
        results[vector.name] = (score, level)
    return results
