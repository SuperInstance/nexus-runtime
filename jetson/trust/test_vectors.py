"""NEXUS Trust Engine - Cross-language test vectors.

These test vectors define inputs and EXACT expected outputs that MUST produce
identical results in C, Python, and TypeScript implementations of the INCREMENTS
trust algorithm.

Golden reference values were generated by running each vector through the Python
IncrementTrustEngine and recording exact numerical results.
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class GoldenReference:
    """Exact expected output from running a test vector through the engine."""

    final_score: float
    final_level: int


@dataclass
class TrustVectorSpec:
    """Specification for a cross-language trust engine test vector.

    Each vector defines a sequence of events and the exact expected outputs.
    Events are (event_type, value) tuples where:
      - ("GOOD", quality): good event with given quality (0.0-1.0+)
      - ("BAD", severity): bad event with given severity (0.0-1.0)
      - ("IDLE", 0): idle tick (decay window with neutral event)
    """

    name: str
    events: list[tuple[str, float]]
    golden: GoldenReference
    note: str = ""
    subsystem: str = "default"


def run_vector(
    events: list[tuple[str, float]],
    subsystem: str = "default",
) -> tuple[float, int]:
    """Execute events through the Python TrustEngine.

    Returns (final_score, final_level).
    Call from within the jetson/ package (pythonpath includes jetson/).
    """
    from trust.increments import IncrementTrustEngine, TrustEvent

    engine = IncrementTrustEngine()
    engine.register_subsystem(subsystem)

    for event_type, value in events:
        if event_type == "GOOD":
            evt = TrustEvent.good("heartbeat_ok", value, subsystem=subsystem)
        elif event_type == "BAD":
            evt = TrustEvent.bad("reflex_error", value, subsystem=subsystem)
        elif event_type == "IDLE":
            evt = TrustEvent(
                "system_boot",
                quality=0.0,
                severity=0.0,
                timestamp=0.0,
                subsystem=subsystem,
                is_bad=False,
            )
        else:
            raise ValueError(f"Unknown event type: {event_type}")

        engine.evaluate_window(subsystem, [evt])

    return engine.get_trust_score(subsystem), engine.get_autonomy_level(subsystem)


def run_vector_detailed(
    events: list[tuple[str, float]],
    subsystem: str = "default",
) -> dict:
    """Execute events and return detailed results including intermediates."""
    from trust.increments import IncrementTrustEngine, TrustEvent

    engine = IncrementTrustEngine()
    engine.register_subsystem(subsystem)

    intermediate_scores: list[float] = []
    intermediate_levels: list[int] = []

    for event_type, value in events:
        if event_type == "GOOD":
            evt = TrustEvent.good("heartbeat_ok", value, subsystem=subsystem)
        elif event_type == "BAD":
            evt = TrustEvent.bad("reflex_error", value, subsystem=subsystem)
        elif event_type == "IDLE":
            evt = TrustEvent(
                "system_boot",
                quality=0.0,
                severity=0.0,
                timestamp=0.0,
                subsystem=subsystem,
                is_bad=False,
            )
        else:
            raise ValueError(f"Unknown event type: {event_type}")

        engine.evaluate_window(subsystem, [evt])
        intermediate_scores.append(engine.get_trust_score(subsystem))
        intermediate_levels.append(engine.get_autonomy_level(subsystem))

    return {
        "final_score": engine.get_trust_score(subsystem),
        "final_level": engine.get_autonomy_level(subsystem),
        "intermediate_scores": intermediate_scores,
        "intermediate_levels": intermediate_levels,
    }


